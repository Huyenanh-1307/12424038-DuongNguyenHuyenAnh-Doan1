-- ================= TABLE =================
CREATE TABLE TaiKhoan (
    MaTK NVARCHAR(10) PRIMARY KEY,
    TenDangNhap NVARCHAR(50) UNIQUE NOT NULL,
    MatKhau NVARCHAR(100) NOT NULL,
    VaiTro NVARCHAR(20) CHECK (VaiTro IN ('Admin', 'NhanVien'))
);

CREATE TABLE NhanVien (
    MaNV NVARCHAR(10) PRIMARY KEY,
    HoTen NVARCHAR(100),
    DienThoai NVARCHAR(15),
    MaTK NVARCHAR(10) UNIQUE,
    FOREIGN KEY (MaTK) REFERENCES TaiKhoan(MaTK)
);

CREATE TABLE Phong (
    MaPhong NVARCHAR(10) PRIMARY KEY,
    TenPhong NVARCHAR(50),
    SoNguoiToiDa INT,
    DangO INT,
    GiaPhong DECIMAL(10,2),
    TrangThai NVARCHAR(30),
    LoaiPhong NVARCHAR(10)
);

CREATE TABLE SinhVien (
    MaSV NVARCHAR(10) PRIMARY KEY,
    HoTen NVARCHAR(100),
    NgaySinh DATE,
    GioiTinh NVARCHAR(10),
    DienThoai NVARCHAR(15),
    Truong NVARCHAR(100),
    MaPhong NVARCHAR(10),
    FOREIGN KEY (MaPhong) REFERENCES Phong(MaPhong)
);

CREATE TABLE HopDong (
    MaHD NVARCHAR(10) PRIMARY KEY,
    MaPhong NVARCHAR(10),
    NgayBatDau DATE,
    NgayKetThuc DATE,
    FOREIGN KEY (MaPhong) REFERENCES Phong(MaPhong)
);

CREATE TABLE ViPham (
    MaVP NVARCHAR(10) PRIMARY KEY,
    MaSV NVARCHAR(10),
    NoiDung NVARCHAR(255),
    TienPhat DECIMAL(10,2),
    NgayViPham DATE,
    LoaiViPham NVARCHAR(100),
    MucCanhBao INT,
    TrangThai NVARCHAR(50),
    GhiChu NVARCHAR(255),
    FOREIGN KEY (MaSV) REFERENCES SinhVien(MaSV)
);

CREATE TABLE ChiSoDienNuoc (
    MaChiSo NVARCHAR(10) PRIMARY KEY,
    MaPhong NVARCHAR(10),
    Thang INT,
    Nam INT,
    DienCu INT,
    DienMoi INT,
    NuocCu INT,
    NuocMoi INT,
    FOREIGN KEY (MaPhong) REFERENCES Phong(MaPhong)
);

CREATE TABLE HoaDon (
    MaHDON NVARCHAR(10) PRIMARY KEY,
    MaHD NVARCHAR(10),
    MaChiSo NVARCHAR(10),
    GiaPhong DECIMAL(18,0),
    MaPhong NVARCHAR(10),
    TongTien DECIMAL(12,2),
    NgayLap DATE,
    FOREIGN KEY (MaHD) REFERENCES HopDong(MaHD),
    FOREIGN KEY (MaChiSo) REFERENCES ChiSoDienNuoc(MaChiSo),
    FOREIGN KEY (MaPhong) REFERENCES Phong(MaPhong)
);
